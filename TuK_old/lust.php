<h1>IM TANZ WACHSEN</h1>
<div class="eintrag">

<p>
Die Tanzform der HKiT&#174; gibt uns die M&ouml;glichkeit, Bewegungsmuster zu erforschen, den eigenen K&ouml;rper kennenzulernen und vor allem: den Wegen des K&ouml;rpers liebevoll zu folgen und Vertrauen in uns selbst zu entwickeln. Bewegung im Tanz erlaubt uns, unsere emotionale als auch k&ouml;rperliche Erlebnisf&auml;higkeit zu erweitern und l&auml;sst die N&auml;he zu uns selbst und zu den anderen entstehen, ein Gef&uuml;hl von Aufgehoben- und Getragensein in der Gruppe, in der Gemeinschaft. 
<br /><br />
Lust an Bewegung, verbunden mit einf&uuml;hlsamen K&ouml;rperwahrnehm&uuml;bungen, &ouml;ffnet uns Tore f&uuml;r m&auml;chtige Tanzarchetypen, die mit ihrem heilsamen Potential Blockaden und alte Muster aufl&ouml;sen k&ouml;nnen. Der Tanz mit allen K&ouml;rperteilen und K&ouml;rperebenen (Knochen, Muskeln, Faszien ...) von Kopf bis Fu&szlig; f&uuml;hrt uns in einen gro&szlig;en Bewegungsreichtum und stellt tiefe Heilungsebenen zur Verf&uuml;gung. 
<br /><br />
Der Tanz, der an keine feste Choreografie gebunden ist, f&uuml;hrt uns zur Entdeckung der eigentlichen Pers&ouml;nlichkeit, von der aus die innere Entfaltung und Sicherheit zu wichtigen Orientierungspunkten im Leben werden. Wir lernen, was es hei&szlig;t, im Tanzen zu wachsen und den allt&auml;glichen Lebenssituationen in Zeiten besonderer Herausforderung sowohl im pers&ouml;nlichen als auch im beruflichen Bereich t&auml;nzerisch zu begegnen. &Uuml;ber den Tanz bekommen wir die gro&szlig;en und die kleinen Antworten auf die Fragen, die uns besch&auml;ftigen.

