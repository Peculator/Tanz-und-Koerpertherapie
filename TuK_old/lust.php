<p>Ein Tanzangebot f&uuml;r Frauen, die ihren K&ouml;rper in der Bewegung, im Tanz neu erfahren m&ouml;chten. Das Tanzen m&ouml;chte Sie dabei unterst&uuml;tzen, sich in allen Schichten und Dimensionen des Daseins wahrzunehmen, von hier aus das innere Wachstum zu f&ouml;rdern. Wir arbeiten mit der  Methode HEILENDEN KR&Auml;FTE IM TANZ&#174;.<br /><br />

