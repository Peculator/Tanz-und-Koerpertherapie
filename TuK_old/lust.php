<div class="eintrag">
<p>

Das Tanzen mit dem ganzen K&ouml;rper und allen Sinnen verschafft uns einen Eingang in eine Gegenwart, eine Gegenwart, in der alles gut und richtig ist. Kein Medium ist f&uuml;r diese Lebensqualit&auml;t und das Gef&uuml;hl von tiefster Zufriedenheit und Lebenslust geeigneter als der Tanz selbst. Die Bewegung im Tanz bedeutet, einen Ausdruck f&uuml;r das finden, was oft im Laufe der Lebensgeschichte unaussprechbar schien. 
<br /><br />
</p>
<p>
Wir werden uns in der Gruppe dem freien Bewegungsimpuls hingeben und ganz innen bei uns sein, um aus der Quelle unserer Lebensenergie einen Tanz von innen nach au&szlig;en entstehen zu lassen. Kippen, Wiegen, Kreisen als urspr&uuml;nglichen lebendigen Ausdruck des Beckens, des K&ouml;rpers wiederentdecken und in den eigenen Tanz integrieren. Hier geht es nicht um das m&uuml;hsame Erlernen neuer Methoden, sondern um das bereits in jeder von uns tief verankerte urspr&uuml;ngliche Bewegungs- und K&ouml;rperbewusstsein. 
</p>
