									<h1>TIEFE VERBUNDENHEIT MIT DEM KÖRPER</h1>
									Das besondere am Tanz ist, nicht eine bestimmte Abfolge von Bewegungen, von darstellerischen Elementen nachzutanzen, sondern eine tiefe Verbundenheit mit dem Körper herzustellen und daraus einen Tanz entstehen zu lassen. Eine Bewegung, die aus dem Erspüren von Körperlandschaften erwächst, lässt uns uns anders bewegen als zu einer Choreografie. Der Tanz entwickelt sich aus der Unmittelbarkeit des Augenblicks heraus, aus dem, was wir in achtsamer Art und Weise in unserem Körper in diesem Moment wahrnehmen und den Prozess eines ganzheitlichen Selbstausdrucks zu fördern. Auf diese Art lernen wir, was es heißt, im Tanzen zu wachsen und den alltäglichen Lebenssituationen in Zeiten besonderer Herausforderung sowohl im persönlichen als auch im beruflichen Bereich tänzerisch zu begegnen.
									</br></br>
									Mit jeder Bewegung wird unsere ganz individuelle Geschichte bewegt. Die uns so vertrauten Bewegungs-, Beziehungs-, Verhaltens-, Denk- und Glaubensmuster können im Tanz immer wieder entbunden und sich aufs Neue verbinden lassen. Aus dieser Verbindung von Bewegung, Körperlichkeit und Atem heraus können wir unsere elementare Ganzheit spüren. 
									<h1>IM TANZ WACHSEN</h1>
									Wir werden uns in der Gruppe dem freien Bewegungsimpuls hingeben und ganz innen bei uns sein, um aus der Quelle unserer Lebensenergie einen Tanz von innen nach außen entstehen zu lassen. Kippen, Wiegen, Kreisen als ursprünglichen lebendigen Ausdruck des Beckens, des ganzen Körpers wiederentdecken und in den eigenen Tanz integrieren. Hier geht es nicht um das mühsame Erlernen neuer Tanzrichtungen, sondern um die Anbindung an das bereits in jeder von uns tief verankerte ursprüngliche Bewegungs- und Körperbewusstsein. Die Basics der Bewegungen, das Hin und Her, das Vor und Zurück und das Kreisen finden sich auch in den Bewegungen des Lebens, in all unseren Gefühlen wieder. Wer kennt es nicht, wenn die Wellen der Freude, des Glücks uns fast überschwemmen und schon im nächsten Augenblick die Welle sich zurückzieht und wir in eine tiefe Trauer zurückfallen. </br></br>
Der Tanz verbunden mit einfühlsamen Körperwahrnehmungsübungen öffnet uns Tore für mächtige Tanzarchetypen. Tanzarchetypen sind wichtige positive Vorbilder, Vortänzerinnen, die Wege aufzeigen können, alte Muster aufzulösen, und uns einladen, unsere eigenen Wege zu gehen. Das sind tänzerische Bewegungen, die wir in den Kulturen um den ganzen Erdball wiederfinden. Fallen, stampfen, drehen, fließen, schlängeln ..., all diese Bewegungen enthalten große Potentiale und führen uns in einen neuen Bewegungsreichtum. </br></br>

Der Tanz mit allen Sinnen, mit dem Atem, den Körperteilen und Körperebenen (Knochen, Muskeln, Faszien …) von Kopf bis Fuß führt uns zu einem neuen Körperbewusstsein und stellt tiefe Heilungsebenen zur Verfügung. Diese Körperlichkeit ist es, die den großen Unterschied macht zu Bewegungen, die einstudiert werden. Denn alle unsere Körperteile stehen miteinander in Verbindung und werden von unserer Lebensenergie in unterschiedlicher Intensität und unterschiedlichem Rhythmus durchflutet. Mit dem Atem fängt alles an zu pulsieren, weitet sich in uns aus und zieht sich wieder zusammen – Innen- und Außenraum werden zu einer Einheit, aus der ein unermesslicher Tanz voller Zauber und Staunen entsteht.  </br></br>

Die Begegnung mit uns selbst gibt uns die Möglichkeit, uns immer wieder neu zu begegnen, all die Bewegungsmuster zu erforschen, diese zu erweitern, den eigenen Körper mit seiner unermesslichen Erlebnisfähigkeit kennenzulernen und vor allem: den Wegen des Körpers liebevoll zu folgen und Vertrauen in uns selbst zu entwickeln. Bewegung im Tanz erlaubt uns, die Nähe zu uns und eine tiefe Verbundenheit zu uns selbst entstehen zu lassen, ein Gefühl von Aufgehoben- und Getragensein im eigenen Körper, in der Gruppe, in der Gemeinschaft. </br></br>

