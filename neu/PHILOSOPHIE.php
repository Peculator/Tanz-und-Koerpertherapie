								<h1>EINE GELUNGENE KOMMUNIKATION</h1>
								Die Tanzform schafft ein hohes Bewusstsein für den eigenen Körper und lässt uns in der Bewegung
								einen Ausdruck für das finden, was oft im Laufe der Lebensgeschichte unaussprechbar schien.
								Mittels Tanz und der achtsamen Körperarbeit wird das feine Zusammenspiel zwischen dem
								Körperausdruck, Körperwahrnehmung und der Seele wieder wahrgenommen und in den Mittelpunkt des
								Geschehens gestellt, all die Bewegungen haben dann die Kraft, eine ausgewogene Harmonie zwischen
								dem Bewusstsein und dem Unbewussten herzustellen – <span style="font-style:italic">so kann über den Körper die Seele heilen.</span> </br></br>
								Schon immer haben die Menschen ihre tiefsten Gefühle und Emotionen durch den Tanz ausgedrückt. Im Tanz verbinden sich
