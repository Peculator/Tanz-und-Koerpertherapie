								<h1>DIE SELBSTHEILUNGSKRÄFTE DES KÖRPERS</h1>
								Heilende Kräfte im Tanz® wurde entwickelt von Gabriele Fischer.
								Die Wurzeln dieser Arbeit gehen zurück auf Dr. F. Goodman (Kulturanthropologin),
								Wilhelm Reich (Bioenergetik) und Gerda Boyesen (Biodynamik).Die besondere Qualität und
								Heilkraft dieser Tanzform und die damit verbundene Körperarbeit liegt im bedingungslosen
								Folgen der Lebensenergie; hier wird nichts manipuliert und diktiert, sondern es wird der
								Bewegung, die von innen herauskommt, gefolgt. Auf dieser Basis können die Selbstheilungskräfte
								des Körpers aktiviert und wichtiges Handwerkszeug wie Vertrauen und neue ungeahnte Beweglichkeit
								mit auf den Weg gegeben werden. </br></br>
								Die Tanz und Körperpsychotherapie ist eine tiefenpsychologisch fundierte Therapieform, die den ganzen Körper in den
								Entfaltungs- und therapeutischen Prozess miteinbezieht. Die Verbundenheit zum Körper in seiner ganzen Komplexität ist
								die Grundlage für ein glückliches und erfülltes Leben. Im Körper sind all unsere Gefühle verankert, und alle Gefühle
								sind ein Ausdruck unserer Lebensenergie. Diese können als treibende und gestaltende Kraft im Leben verstanden werden.
								Durch Verletzungen ( emotional, physisch...), traumatische Erfahrungen kommt es oft dazu, dass wir in unserer vitalen
								Bewegung gestört werden, (Bewegungen werden angehalten, unterbrochen oder gar nicht ausgeführt) und unseren Körper in
								Folge von Schmerzen nicht mehr spüren können, wodurch uns auch ein großes Stück an Lebensqualität und Beweglichkeit
								verloren geht. </br>
								<h1>DIE SEELE BERÜHREN</h1>
								Die Biodynamische Körperpsychotherapie wurde von der norwegischen Psychologin und Physiotherapeutin Gerda Boyesen
								entwickelt und ist eine der ältesten tiefenpsychologisch fundierten körperorientierten Therapieformen in Europa.
								Diese basiert auf den Forschungsarbeiten von Wilhelm Reich. Reich entdeckte, dass sich neurotische Muster im Körper
								in Form von muskulären Spannungen zeigten, er prägte den Begriff Muskelgewebepanzer. Gerda Boyesen stellte weitere
								Verbindung zwischen psychoanalytischem Denken und den von ihr angewandten sanften biodynamischen Massagetechniken her
								und erkannte, dass sich neurotische Störungen nicht nur im Muskel, sondern auf allen Ebenen im Körper in Form von
								Stoffwechselrückständen manifestieren können und einen Gewebepanzer bilden (Haut, Knochen, Flüssigkeit). Der Begriff
								„biodynamisch“ drückt den natürlich fließenden Strom der Lebensenergie aus, welcher durch unseren Körper hindurchfließt
								und damit in der Lage ist, uns zu bewegen, der uns lebendig macht. Die Biodynamik geht davon aus, dass jeder Mensch
								mit der Fähigkeit der Selbstregulation geboren wurde, und sich nach Stress oder traumatischen Erlebnissen in
								Gleichgewicht wieder bringen kann.</br></br>
								Peter Levine (Begründer des Somatic Experiencing, inzwischen eine der wichtigsten und weltweit verbreiteten Methoden
								zur Behandlung von traumatischen Erlebnissen) hat nachweisen können, dass im Körper nach einer traumatischen Erfahrung
								noch eine Restenergie im Körper, im Gewebe, verbleibt und dass diese die wesentliche Ursache für viele körperlichen und
								psychischen Symptome ist. Viele Menschen berichten davon, dass sie nach einer verbalen Psychotherapie (Therapieform,
								in der hauptsächlich gesprochen wird) noch immer an den Symptomen leiden und sich im Wesentlichen nichts verändert
								hat –  <span style="font-style:italic">die Seele blieb unberührt. </span></br></br>
								Gerade dann ist es wichtig, die körperlichen Reaktionen zu berücksichtigen, und hier löst der Tanz die Blockierungen in
								der Bewegung auf. Damit werden die Selbstheilungskräfte im Körper aktiviert. Das ist ein wichtiger Beitrag zur Heilung
								und Wachstum der Persönlichkeit –  <span style="font-style:italic">so kann über den Körper die Seele heilen.</span> </br>
