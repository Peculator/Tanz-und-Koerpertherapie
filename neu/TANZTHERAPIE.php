								<h1>DER WUNSCH NACH VERÄNDERUNG</h1>
								Das Ziel der tanztherapeutischen Gruppe ist, in einem geschützten Raum achtsam den gegenwärtigen Augenblick,
								das Hier und Jetzt, zu erleben und die Verbindung zwischen Körper, Lebensenergie und Seele wieder-herzustellen.
								Das Sich-Erleben hilft uns, uns tiefergehend wahrzunehmen – unsere Gefühle, Bedürfnisse, Wünsche, Gedanken.
								In der Bewegung, in geführten Meditationen, in der Atem- und Stimmarbeit sowie bei biodynamischen Massagen setzen
								wir uns mit unseren persönlichen Themen, unbefriedigenden Lebenssituationen und dem Wunsch nach Veränderung auseinander.</br></br>
								Bei Verletzungen körperlicher oder seelischer Art, sowohl in der Gegenwart als auch in der Vergangenheit,
								bei belastenden Situationen, die nicht angemessen begleitet wurden, müssen die Gefühle, oft aufgrund des
								kaum aushaltbaren Schmerzes, vom Bewusstsein und vom Körper verdrängt werden. Die Folgen davon sind: der
								Kontaktverlust zu uns selbst, Bewegungseinschränkungen, Verlust an Spontanität, Veränderung in der Körperhaltung
								sowie Veränderung in der Atmung. </br></br>
								<h1>DIE HEILKRAFT DER BEWEGUNG</h1>
								Der Ansatz der Heilenden Kräfte im Tanz® richtet sich in empathischer Grundhaltung ohne Umwege
								über die Bewegung direkt an den Körper. Damit werden die psychoemotionalen Zusammenhänge ersichtlich
								und in der Annahme des gegenwärtigen Augenblicks die Selbstheilungskräfte in uns aktiviert. In der Gemeinschaft
								mit anderen können korrigierende Erfahrungen gemacht werden, Erfahrungen, die uns berühren, die neue Lösungswege
								aufzeigen und so in einen ganzheitlichen Heilungsprozess eingewoben werden. Damit sind die besten Voraussetzungen
								für einen gelungenen Heilungsprozess gegeben.</br></br>
								Bewegung, verbunden mit einfühlsamen Körperwahrnehmungsübungen, öffnen uns Tore für mächtige Tanzarchetypen. Tanzarchetypen sind wichtige Vorbilder
								für uns, die Wege aufzeigen können, alte Muster aufzulösen. Das sind tänzerische Bewegungen, die wir in den Kulturen um den ganzen Erdball wiederfinden.
								Fallen, stampfen, drehen, fließen, schlängeln..., all diese Bewegungen enthalten große Potentiale und führen uns in einen neuen Bewegungsreichtum.
								Der Tanz mit allen Körperteilen und Körperebenen (Knochen, Muskeln, Faszien...) von Kopf bis Fuß führt uns zu einem neunen Körperbewusstsein und stellt
								tiefe Heilungsebenen zur Verfügung. </br></br>
								<h1>WICHTIGE VERBINDUNGEN SCHAFFEN</h1>
								In der menschlichen Entwicklung durchlaufen wir verschiedene Phasen, die unterschiedlich
								bewältigt werden. Ein Tanz, eine Bewegung zwischen Verlangen und Sättigung, der mit dem
								ganzen Körper verkörpert, erlebt und erfahren wird. Hier erleben wir unseren Körper als
								einen sicheren Ort, in dem wir geliebt und aufgehoben sind, machen wichtige Erfahrungen,
								die richtungweisend für das spätere Leben sind. </br></br>
								Die Bindung zu den ersten Bezugspersonen spielt dabei eine zentrale Rolle; sie wirkt sich später auf alle anderen Bindungen
								und Beziehungen aus. Bei einer nicht angemessenen Unterstützung in den einzelnen Entwicklungsphasen, einem Übermaß an
								Resonanzlosigkeit, Enttäuschungen und emotionaler Kälte entsteht ein Mangel, der sich in die Körperstrukturen einprägt
								und tiefe Spuren hinterlässt. Nicht selten ist die Folge davon ein Verlust an Lebendigkeit, ein Verlust der Verbindung
								zu uns selbst, der Verbindung zu unserem Körper und in der Folge der Verbindung mit all denen, die uns umgeben.
								Psychosomatische Symptome und andere Krankheiten sind oft die Signale unserer Seele, dass wir nicht im Kontakt
								mit unserer Primärpersönlichkeit sind.  </br></br>
								<h1>EINE HEIMAT IM KÖRPER FINDEN</h1>
								Hier in der Gruppe haben wir die Möglichkeit, korrigierende Erfahrungen zu machen, in dem wir bedingungslos dem
								Fluss der Lebensenergie folgen, ohne dass es zu erneuten Verletzungen kommt. Die Heilkraft der Bewegung im Tanz,
								der Ausdruck über die Bewegung und die Achtsamkeit für die Wahrnehmung der Empfindungen im Körper lassen eine Heimat
								im Körper entstehen, in der Verbundenheit zu uns selbst und dem Eingebundensein in der Gemeinschaft mit anderen
								Menschen. </br></br>
								Die Tanztherapie hebt im Gegensatz zu anderen therapeutischen Ansätzen die Eigenverantwortlichkeit und
								Selbstbestimmtheit des körpertherapeutischen Prozesses hervor. Ich selbst bin es, die tanzt und damit den
								therapeutischen Prozess bestimmt. Heilende Kräfte im Tanz® kann aus diesem Grund ganz auf manipulative,
								eingreifende und grenzverletzende Techniken verzichten. Dies ist ein Grund für die große Wirksamkeit dieser
								Methode. Dabei ist die einfühlsame Arbeit mit ihren reichhaltigen Angeboten ressourcenorientiert, das heißt,
								es wird das Positive, das Heile, das Ganze und das Kompetente in dem Menschen gesehen, gestärkt und unterstützt. </br></br>
